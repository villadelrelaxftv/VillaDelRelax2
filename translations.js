(function () {
  function applyLanguage(language) {
    const selectedLanguage = VDR_TRANSLATIONS[language]
      ? language
      : "es";

    const dictionary = VDR_TRANSLATIONS[selectedLanguage];

    document.documentElement.lang = selectedLanguage;
    document.title = dictionary.pageTitle;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const key = element.dataset.i18n;

      if (Object.prototype.hasOwnProperty.call(dictionary, key)) {
        element.textContent = dictionary[key];
      }
    });

    const languageSelect = document.getElementById("languageSelect");

    if (languageSelect) {
      languageSelect.value = selectedLanguage;
    }

    localStorage.setItem("vdrLanguage", selectedLanguage);
  }

  window.changeLanguage = applyLanguage;

  document.addEventListener("DOMContentLoaded", () => {
    const languageSelect = document.getElementById("languageSelect");
    const savedLanguage = localStorage.getItem("vdrLanguage") || "es";

    applyLanguage(savedLanguage);

    if (languageSelect) {
      languageSelect.addEventListener("change", (event) => {
        applyLanguage(event.target.value);
      });
    }
  });
})();
